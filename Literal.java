class Literal{
    public static void main(String args[]){
        byte age = 34;
        // '34' here is a literal ie a constant value assgined to the variable
        // byte, short, int are declared by default
        char c = 'A';
        // 'A' here is a literal
        float f1 = 5.6f;
        // 'f','F' is added to make it float-type literal
        double d1 = 6.66;
        // decimal value by default is double, but we can add 'd','D' if we want to
        long l1 = 2342;
        // 'L' or 'l' can be added, not mandatory
        String str = "Hello";
        System.out.println(str);
    }
}